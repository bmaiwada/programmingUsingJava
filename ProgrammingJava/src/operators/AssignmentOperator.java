package operators;

public class AssignmentOperator {
    public static void main(String[] args) {
        int numOne = 10;
        System.out.println(numOne);

        numOne *= 5;
        System.out.println(numOne);

        numOne += 5;
        System.out.println(numOne);

        numOne -= 5;
        System.out.println(numOne);

        numOne /= 5;
        System.out.println(numOne);

    }
}
