package operators;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int numOne = 10;
        int numTwo = 5;

        System.out.println(numOne + numTwo);
        System.out.println(numOne - numTwo);
        System.out.println(numOne * numTwo);
        System.out.println(numOne / numTwo);
        System.out.println(numOne % numTwo);

    }
}
